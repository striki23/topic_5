numbers = [17, 6.06534, 91, 52, 87, 340, 56]

# Находим сумму всех значений в списке numbers
sum_num = sum(numbers)

# Находим максимальный элемент в списке numbers
mx_num = max(numbers)

# Находим минимальный элемент в списке numbers
min_num = min(numbers)

# Выводим результат в консоль

print('Сумма элементов списка: ', sum_num)
print('Максимальный элемент списка: ', mx_num)
print('Минимальный элемент списка: ', min_num)
